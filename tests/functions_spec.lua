local config = require("pix-nvim-study.config")
local functions = require("pix-nvim-study.functions")

describe("functions", function()
	before_each(function()
		config.setup()
	end)
	describe("vim.cmd", function()
		before_each(function()
			vim.print("vim.cmd before_each")
		end)
		it("vim.cmd test aaa == bbb", function()
			assert.equals("aaa", "bbb")
			functions.run_function("vim.cmd")
		end)
	end)
end)
